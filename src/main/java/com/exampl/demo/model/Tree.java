package com.exampl.demo.model;

public class Tree {
    private int t_id;
    private String t_name;
    private int t_pid;
    public int getTree_id() {
        return t_id;
    }

    public void setTree_id(int t_id) {
        this.t_id = t_id;
    }



    public String getName() {
        return t_name;
    }

    public void setName(String t_name) {
        this.t_name = t_name;
    }



    public int getPid() {
        return t_pid;
    }

    public void setPid(int t_pid) {
        this.t_pid = t_pid;
    }



}
